    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "Restaurant1", for: indexPath)
        let sqlite = SQLiteManager.sharedInstance
        sqlite.openDB()
        let queryResult = sqlite.execQuerySQL(sql: "SELECT * FROM restaurant WHERE type = '川菜';")
        cell.textLabel?.text = queryResult?[indexPath.row]["name"]! as! String
        cell.detailTextLabel?.text = queryResult?[indexPath.row]["address"]! as! String

        return cell
    }
    

